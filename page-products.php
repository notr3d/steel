				  <p>Technically, the fortifying rope suspensions also can be used with single-standing towers.</p>
				  <p>Usually, steel lattice transmission angle-tension towers are designed for angle of deviation up to 60. The limit rates can be found in erection blueprints and design notes. Also, angle-tension towers can be used as dead-end poles.</p>
				  <p>Nowadays, the unification, made several years ago by design institutes, helps to increase common rate of quality level in lane constructions.</p>
				  <p>Main advantages of steel lattice towers are:</p>
				  <ul>
					  <li>The possibility of designing towers of great heights, high wires connection altitudes, able to resist high mechanical loads;</li>
					  <li>Light weight and high durability in comparison with others towers;</li>
					  <li>Ease of manufacturing and collection on construction sites.</li>
				  </ul>
				  <p>These advantages allow to use lattice towers for every lanes on any currents, including lanes in hard climate and geographic conditions. Also, steel towers can be used as dead-end poles on lines with current from 100 till 500 kV, with ferroconcrete towers as intermediate supports.</p>
				  <p>Usually, steel lattice towers are made with bolts mounting. However, in some cases welding needs to be used.  The towers with bolts mounting are very easy in transportation and galvanizing.</p>
				  <p>The tower details and welded parts have corrosion-resistant coating from environment diverse impact, which is made by galvanizing. Also, metalware has this coating as well, and it is made by galvanizing or thermal diffusion.</p>
				  <p>A power transmission tower consists of the following parts:</p>
				  <ul>
					  <li>Peak of transmission tower</li>
					  <li>Cross arm of transmission tower</li>
					  <li>Boom of transmission tower</li>
					  <li>Cage of transmission tower</li>
					  <li>Transmission Tower Body</li>
					  <li>Leg of transmission tower</li>
					  <li>Stub/Anchor Bolt and Base plate assembly of transmission tower.</li>
				  </ul>
				  <p>All tower details are combined in packages after production. The shipment of these packages is less time-consuming than every other type of packing, as on erection site as on lines construction sites.</p>                    
